// Number data type
let num = 10;
let floatNum = 3.14;
// String data type
let str = 'Hello, ';
let anotherStr = "World!";
// Boolean data type
let isTrue = true;
let isFalse = false;
// Undefined data type
let undefinedVar;
// Null data type
let nullValue = null;
// Symbol data type
const sym1 = Symbol('description');
const sym2 = Symbol('description');
// Object data type
let person = {
    name: 'John',
    age: 30,
    city: 'New York'
};
// Function data type
function greet(name) {
    return `Hello, ${name}!`;
}
// Printing data types and their values
console.log('Number:', num, floatNum);
console.log('String:', str + anotherStr);
console.log('Boolean:', isTrue, isFalse);
console.log('Undefined:', undefinedVar);
console.log('Null:', nullValue);
console.log('Symbol:', sym1, sym2);
console.log('Object:', person);
console.log('Function:', greet('Alice'));

